import type { CefrLevel, PlacementSkill } from "../types";
import type { ItemBlueprint } from "./factory";

type TrackTag = ItemBlueprint["relatedTrackTags"][number];
type TargetInput = ItemBlueprint["targets"][number];

type Entry = {
  context: string;
  prompt: string;
  correct: string;
  distractors: [string, string, string];
  tags?: string[];
};

type PackInput = Omit<ItemBlueprint, "targets"> & {
  entries: Entry[];
};

const vocabularyFrames = [
  (entry: Entry) =>
    `Complete the sentence for ${entry.context}: "${entry.prompt}"`,
  (entry: Entry) =>
    `A learner needs natural wording for ${entry.context}. Choose the best option: "${entry.prompt}"`,
  (entry: Entry) =>
    `Which option fits this situation? ${entry.context}: "${entry.prompt}"`,
  (entry: Entry) =>
    `Choose the natural English completion for ${entry.context}: "${entry.prompt}"`,
] as const;

const grammarFrames = [
  (entry: Entry) =>
    `Complete the sentence for ${entry.context}: "${entry.prompt}"`,
  (entry: Entry) =>
    `A learner is writing about ${entry.context}. Choose the form that fits: "${entry.prompt}"`,
  (entry: Entry) =>
    `Which option completes the sentence naturally for ${entry.context}? "${entry.prompt}"`,
  (entry: Entry) =>
    `Choose the grammatically accurate completion for ${entry.context}: "${entry.prompt}"`,
] as const;

function targetsFromEntries(
  entries: Entry[],
  frames: typeof vocabularyFrames | typeof grammarFrames,
): TargetInput[] {
  return entries.flatMap((entry) =>
    frames.map((frame, index) => ({
      context: entry.context,
      stem: frame(entry),
      correct: entry.correct,
      distractors: entry.distractors,
      explanation: `${entry.correct} is the natural choice in this context.`,
      feedbackIfWrong:
        "The other choices reflect common learner errors in form, collocation, meaning, or register.",
      teachingImplication:
        "Review this point through short contextual examples, retrieval practice, and corrected speaking or writing.",
      recommendationTags: [
        ...(entry.tags ?? []),
        index % 2 === 0 ? "context-control" : "retrieval-practice",
      ],
    })),
  );
}

function pack(input: PackInput, skill: PlacementSkill): ItemBlueprint {
  return {
    construct: input.construct,
    subskill: input.subskill,
    microSkill: input.microSkill,
    itemType: input.itemType,
    targets: targetsFromEntries(
      input.entries,
      skill === "vocabulary" ? vocabularyFrames : grammarFrames,
    ),
    explanation: input.explanation,
    feedbackIfWrong: input.feedbackIfWrong,
    teachingImplication: input.teachingImplication,
    recommendationTags: input.recommendationTags,
    relatedTrackTags: input.relatedTrackTags,
  };
}

